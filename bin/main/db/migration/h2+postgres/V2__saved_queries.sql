-- History page: saved query console URLs (FK to users). Same script for H2 and PostgreSQL.
CREATE TABLE saved_queries (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    label VARCHAR(512) NOT NULL DEFAULT '',
    console_query VARCHAR(10000) NOT NULL,
    mode VARCHAR(32) NOT NULL DEFAULT 'search',
    summary VARCHAR(512),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_saved_queries_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE
);

CREATE INDEX idx_saved_queries_user_created ON saved_queries (user_id, created_at DESC);
